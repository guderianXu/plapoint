template <typename Scalar>
Scalar interp(Scalar iso, Scalar v0, Scalar v1, Scalar p0, Scalar p1)
{
    if (std::abs(v1 - v0) < Scalar(1e-12)) return p0;
    return p0 + (p1 - p0) * (iso - v0) / (v1 - v0);
}

